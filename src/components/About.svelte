<h3>About The Project</h3>
<p>I just have doing an Svelte 3 example blog that have:</p>
<ol>
    <li>Header with the menu or navigation bar</li>
    <li>A fake login with 10 diferent random users got from the api</li>
    <li>Main content area with all latest blog posts or posts by the logged in user</li>
    <li>An article page to show details about the chosen post</li>
    <li>A profile page with details about the current user</li>
    <li>A About page with technical information about the technologies used</li>
    <li>Footer with relevant links and contacts from me</li>
</ol>

